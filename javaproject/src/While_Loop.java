public class While_Loop {
    public static void main(String[] args) {

        //     while(condition){code}
        int i=0;

        while (i<5){
            System.out.println("Hello "+i);
            i++;

        }
        //do {code}
        // while(condition);

        int j=10;
        do{
            System.out.println("Hello with do while "+j);
            j++;
        }while (j<5);


    }
}
