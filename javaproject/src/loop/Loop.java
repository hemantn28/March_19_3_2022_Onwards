package loop;

public class Loop {
    public static void main(String[] args) {
        for(int i=0; i<5; i++){
            System.out.println("Hello");
        }
        for(int i=0; i<9; i++){
            System.out.println("hello");
        }

        for(int i=0; i<1000; i++){
            System.out.println("India is Great");
        }
        for(int i=2; i<8; i++){
            System.out.println("Chaos");
        }
        for(int i=1; i<6; i++){
            System.out.println("hello "+i);
        }
        for(int i=5; i>0; i--){
            System.out.println("hello "+i);
        }
        for(int i=10; i>0; i--){
            System.out.println("Hello "+i);
        }

    }



}
