package loop;

public class TableOfSix {
    public static void main(String[] args) {

        for(int i=0; i<10; i++){
            System.out.println(i);
        }

        for(int i=0; i<7; i++){
            System.out.println(i);
        }
        for(int i=9; i>0; i--){
            System.out.println(i);
        }

        for(int i=15; i>10; i--){
            System.out.println(i);
        }
        for(int i=20; i>17; i--){
            System.out.println(i);
        }
        for(int i=1; i<9; i++){
            System.out.println(i);
        }
        for(int i=5; i<8; i++){
            System.out.println(i);
        }




    }
}
