package loop;

public class DoWhile {
    public static void main(String[] args) {
        /*syntax for do while loop: do{code}
                                      while(condition);
         */
        int i=0;
        do{
            System.out.println("i am cool");
            i++;
        }
        while(i<5);

        int j=0;
        do{
            System.out.println("hustle");
            j++;
        }
        while(j<3);

        int n=0;
        do{
            System.out.println("crime");
            n++;
        }
        while(n<2);
    }
}
