package class_and_object;

public class Mohan {
    //data member
    String name="Mohan";
    int age=15;
    int standard=10;

    public static void main(String[] args) {
        //creating object=Classname objectname=new Classname();
        Mohan obj=new Mohan();
        System.out.println(obj.name);
        System.out.println(obj.age);
        System.out.println(obj.standard);
    }
}
