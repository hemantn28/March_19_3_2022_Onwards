package while_loop;

public class Java_Std_While {
    public static void main(String[] args) {
        //syntax: while(condition){code}
        int i=14;
        while (i<20){
            System.out.println(i);
            i++;
        }
        //do{code}
        //while(condition);
        int k=5;
        do{
            System.out.println(k);
            k++;

        }while (k<21);
    }
}
