package while_loop;

public class Std_Java_while {
    public static void main(String[] args) {
        //syntax: while(condition){code}
        int i=20;
        while(i<31){
            System.out.println(i);
            i++;
        }
        //do{code}
        //while(condition);
        int o=0;
        do {
            System.out.println(o);
            o++;

        }   while (o<11);
    }
}
