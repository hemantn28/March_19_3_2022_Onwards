package while_loop;

public class Loop_Java_While {
    public static void main(String[] args) {
        //syntax: while(condition){code}
        int i=0;
        while(i<8){
            System.out.println(i);
            i++;
        }
        //do{code}
        //while(condition);
        int l=11;
        do{
            System.out.println(l);
            l++;
        }while(l<16);
    }
}
