package loop;

public class Loop_Class {

    public static void main(String[] args) {


        // SYNTAX: for initialization ; condition;  increment/decrement){
        ////                               code to be executed
        //                                                        }

        for (int i=0; i<5; i++){
            System.out.println("Hello" + i);


        }

        // SYNTAX: for initialization ; condition;  decrement){
        ////                               code to be executed
        //                                                        }

        for (int i=5; i>0 ; i--){
            System.out.println("Hello" +i);
        }
        
        
        





    }
}
