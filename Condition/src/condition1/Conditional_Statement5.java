package condition1;

public class Conditional_Statement5 {

    public static void main(String[] args) {

        int age=15;

        if(age>18){
            System.out.println("eligible for voting");
        }

        else {
            System.out.println("Not eligible for voting");
        }

        String cityname="Moradabad";

        if(cityname=="Amroha"){
            System.out.println("You are in Amroha");
        }

        else {
            System.out.println("You are not in Amroha");
        }

        double price=100;

        if(price>110){
            System.out.println("Product is costly");
        }

        else {
            System.out.println("Product is not costly");
        }

    }
}
